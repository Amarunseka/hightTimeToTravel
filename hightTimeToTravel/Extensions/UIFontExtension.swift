import UIKit

extension UIFont {
    
    static func proximaNovaBlack22() -> UIFont? {
        return UIFont.init(name: "ProximaNova-Black", size: 22)
    }
    
    static func proximaNovaBold16() -> UIFont? {
        return UIFont.init(name: "ProximaNova-Bold", size: 16)
    }
    
    static func proximaNovaRegular26() -> UIFont? {
        return UIFont.init(name: "ProximaNova-Regular", size: 26)
    }
}
